var BallonMesh = function( size, txt ){

    this.side = -1;
    this.zoom = 1;

    this.mesh = new THREE.Group();
    this.mesh.scale.set( size, size, size );

    this.mats = [];


    this.shader = new THREE.ShaderMaterial({
        uniforms: {
            map : { type: 't', value: null },
            color: {type: 'c', value: new THREE.Color(0x323436) },
            angle: {type: 'f', value: 0 },
            zoom: {type: 'f', value: 1 },
        },
        vertexShader:[

        'uniform float angle;',
        'uniform float zoom;',

        'vec2 rotUV(vec2 uv, float angle ){',
        '    float s = sin(angle);',
        '    float c = cos(angle);',
        '    mat2 r = mat2( c, -s, s, c);',
        '    r *= 0.5; r += 0.5; r = r * 2.0 - 1.0;',
        '    uv -= 0.5; uv = uv * r; uv += 0.5;',
        '    return uv;',
        '}',

        'vec2 zoomUV(vec2 uv, float power ){',
        '    uv -= 0.5; uv = uv * power; uv += 0.5;',
        '    return uv;',
        '}',

        'varying vec2 vUv;',
        'void main(){',
        '    vUv = rotUV( uv, angle);',
        '    vUv = zoomUV( vUv, zoom);',
        '    gl_Position = projectionMatrix * modelViewMatrix * vec4(position,1.0);',
        '}'

        ].join('\n'),
        fragmentShader:[

        'uniform sampler2D map;',
        'uniform vec3 color;',
        'varying vec2 vUv;',
        'void main(){',
        '    gl_FragColor = vec4( color, 1.0 );',
        '    gl_FragColor *= texture2D(map, vUv);',
        '}'

        ].join('\n')
    });

    var g, m, d, mat;

    for(var i = 0; i<this.data.length; i++){

        d = this.data[i];

        g = new THREE.BufferGeometry();
        g.name = d.name;
        g.setIndex( new THREE.BufferAttribute( new Uint16Array( d.index ), 1 ));
        g.addAttribute('normal', new THREE.BufferAttribute( new Float32Array( d.normal ), 3 ));
        g.addAttribute('position', new THREE.BufferAttribute( new Float32Array( d.position ), 3 ));
        g.addAttribute('uv', new THREE.BufferAttribute( new Float32Array( d.uv ), 2 ));

        g.computeBoundingBox();
        g.computeBoundingSphere();

        mat = new THREE.ShaderMaterial().copy( this.shader );//.clone();//new THREE.MeshBasicMaterial({map:txt})

        mat.uniforms.map.value = txt;
        mat.uniforms.color.value =  new THREE.Color( Math.random() * 0xffffff );

        m = new THREE.Mesh( g, mat );
        m.name = d.name;

        this.mesh.add( m );
        this.mats.push( mat );

    }

};

BallonMesh.prototype = {

    update:function(){

        if(this.zoom>=1){ this.zoom = 1; this.side = -1 };
        if(this.zoom<0.3){ this.zoom = 0.3; this.side = 1; };

        this.zoom += 0.01*this.side;

        var z = this.zoom

        this.mats.forEach( function ( m ) { 
            m.uniforms.angle.value += 0.01; 
            //var z = m.uniforms.zoom.value;
            


            //if(z>=1){ m.uniforms.zoom.value = 1; s = -1 };
            //if(z<=0.3){ m.uniforms.zoom.value = 0.3; s = 1; };

            m.uniforms.zoom.value =  z;

            
        });

    }

};


